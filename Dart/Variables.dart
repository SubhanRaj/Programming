//  😍Variable in Dart😍
//  Variables is an identifier used to refer memory location in computer memory that holds a value for that variable, this value can be changed during the execution of the program.


// Remember these point given below, during declaration of variables.

// 1) The first character must be an alphabet or underscore.
// 2) An identifer can not start with a digit. 
// 3) All succeeding characters must be alphabets or digits. 
// 4) No special charater or puncutation symbol is allowed except underscore.
// 5) N two successive underscore are allowed. 
// 6) keyword can not be used as identifiers. 

// 😎Declaration using var keyword😎
//  In Dart, The var keyword is used to declare a variable. 
 
// Syntax:
// var <variable_name> = <value>
//      or 
// var <variable_name>;

// Example:
// var name = 'vaibhav goswami';
// var name;
// name = 'vaibhav goswami';

// 😎Declaration using type😎

// Syntax:
// <type> <variable_name>;  
//      or 
// <type> <name> = <expression>;

// Example:

// int number = 56;
// double = 5.6;
// String name = "vaibhav goswami";
// bool isvalid = true;

// 😎Declaration using final and const keyword😎

// When we do not want to change a variable in the future then we use final and const. It can be used in place of var or in addition to a type.

// Example:

// final name = 'vaibhav goswami';
// const PI = 3.14;
// Now we can not change the value of name.

