#include <stdio.h>
// A simple programm for understanding Type of C Instructions

int main()
{
    int a = 4;          // Type declaration instruction
    int b , c;  // Type declaration instruction
    c = b = a;    //Type declaration instruction

    printf("\n");
    printf("The value of numbers entered as Integer a is %d \n,", a);
    printf("\n");
    printf("The value of number entered as Integer b is %d\n", b);
    printf("\n");
    printf("The value of number entered as Integer c is %d\n", c);
    printf("\n");
    
    

    return 0;
}