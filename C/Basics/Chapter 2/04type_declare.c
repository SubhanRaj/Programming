#include <stdio.h>
// A simple programm for understanding Type of C Instructions

int main()
{
    float a = 8.9;          // Type declaration instruction
    float b = 1.1; // Type declaration instruction
    float c = a + b;     //Type declaration instruction

    printf("\n");
    printf("The value of numbers entered as variable a is %f \n,", a);
    printf("\n");
    printf("The value of number entered as variable b is %f\n", b);
    printf("\n");
    printf("The value of number entered as variable c is %f\n", c);
    printf("\n");

    return 0;
}